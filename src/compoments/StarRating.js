import { useState, React } from "react";
const containerStyle = {
  display: "flex",
  alignItems: "center",
  gap: "16px",
};

const starContainerStyle = {
  display: "flex",
  gap: "2px",
};

export const StarRating = ({
  maxRating = 5,
  rating,
  setRating,
  defaultRating = 3,
  fstColor = "yellow",
  secColor = "yellow",
  size = 24,
  className = "",
  showRating = true,
  messages = [],
}) => {
  const [hoverRating, setHoverRating] = useState(
    rating > 0 ? rating : defaultRating
  );

  const textStyle = {
    fontHeight: "1",
    margin: "0px",
    color: `${fstColor}`,
    fontSize: `${size / 1.5}px`,
  };
  const starStyle = {
    cursor: "pointer",
    height: `${size}px`,
    width: `${size}px`,
    display: "block",
  };

  return (
    <div style={containerStyle} className={className}>
      <div style={starContainerStyle}>
        {Array.from({ length: maxRating }, (_, i) => (
          <span
            role="button"
            style={starStyle}
            key={i}
            onClick={() => setRating(i + 1)}
            onMouseOver={() => setHoverRating(i + 1)}
          >
            {i + 1 <= hoverRating ? (
              <FullStar fillColer={fstColor} strokeColor={secColor} />
            ) : (
              <EmptyStar strokeColor={secColor} />
            )}
          </span>
        ))}
      </div>
      {showRating && (
        <p style={textStyle}>
          {console.log("hoverrating: ", hoverRating)}
          {console.log("rating: ", rating)}
          {messages.length === maxRating && messages[hoverRating - 1]}
          {messages.length !== maxRating && (hoverRating || rating || "")}
        </p>
      )}
    </div>
  );
};

const FullStar = ({ fillColer, strokeColor }) => {
  return (
    <svg
      xmlns="http://www.w3.org/2000/svg"
      viewBox="0 0 20 20"
      fill={fillColer}
      stroke={strokeColor}
    >
      <path d="M9.049 2.927c.3-.921 1.603-.921 1.902 0l1.07 3.292a1 1 0 00.95.69h3.462c.969 0 1.371 1.24.588 1.81l-2.8 2.034a1 1 0 00-.364 1.118l1.07 3.292c.3.921-.755 1.688-1.54 1.118l-2.8-2.034a1 1 0 00-1.175 0l-2.8 2.034c-.784.57-1.838-.197-1.539-1.118l1.07-3.292a1 1 0 00-.364-1.118L2.98 8.72c-.783-.57-.38-1.81.588-1.81h3.461a1 1 0 00.951-.69l1.07-3.292z" />
    </svg>
  );
};

const EmptyStar = ({ strokeColor }) => {
  return (
    <svg
      xmlns="http://www.w3.org/2000/svg"
      fill="none"
      viewBox="0 0 24 24"
      stroke={strokeColor}
    >
      <path
        strokeLinecap="round"
        strokeLinejoin="round"
        strokeWidth="{2}"
        d="M11.049 2.927c.3-.921 1.603-.921 1.902 0l1.519 4.674a1 1 0 00.95.69h4.915c.969 0 1.371 1.24.588 1.81l-3.976 2.888a1 1 0 00-.363 1.118l1.518 4.674c.3.922-.755 1.688-1.538 1.118l-3.976-2.888a1 1 0 00-1.176 0l-3.976 2.888c-.783.57-1.838-.197-1.538-1.118l1.518-4.674a1 1 0 00-.363-1.118l-3.976-2.888c-.784-.57-.38-1.81.588-1.81h4.914a1 1 0 00.951-.69l1.519-4.674z"
      />
    </svg>
  );
};

